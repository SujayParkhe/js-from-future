// Maths Operators
const currentYear = 2024;
const skyAge = currentYear - 2020;
const sujayAge = currentYear - 2007;
console.log(skyAge, sujayAge);

// 2 ** 3 means 2 to the power of 3 = 2 * 2 * 2
console.log(skyAge * 2, sujayAge / 2, 2 ** 3);


// String concatenation using + operator
const firstName = 'Sky';
const lastName = 'Parkhe';
console.log(firstName + ' ' + lastName);

// Assignment operators
// Simple Assignment
let x = 10 + 5; // 15
// Addition Assigment
x += 10 // x = x + 10 = 25

x *= 4 // x = x * 4 = 100
console.log(x);

// Comparison Operators
console.log(skyAge < sujayAge);

console.log(sujayAge >= 18);

